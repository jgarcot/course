package com.roche.vnv.first;

/**
 * The "Hello World!" application consists of three primary components: source code comments, the HelloWorld class definition, and the main method.
 * Multiline comment --> Javadoc usage
 */
public class HelloWorld {

    /**
     * The main method it's the entry point for your application and will subsequently invoke all the other methods required by the program
     *
     * Every application must contain a main method
     *
     * The modifiers public and static can be written in either order (public static or static public), but the convention is to use public static
     *
     * @param args which the runtime system passes information to the application
     */
    public static void main(String[] args) {
        System.out.println("Hello World!"); // Display the string
    }
}
